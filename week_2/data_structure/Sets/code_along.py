# Using curly braces
fruits = {"orange", "kiwi", "grapes"}
print(fruits)

# Using set () constructor
nums = set([1, 3, 5, 7])
print(nums)

# Creating an empty set use () not {}
empty_set = set()
print(empty_set)

# From a string (duplicates removed automatically)
letters = set("hippopotamus")
print(letters)

